/* Service Worker v0.6 — network-first */
const CACHE = 'mt4-v0.6';
const STATIC = [
  './data/lessons.json',
  './SRC/components/SequenceOrderTask.js',
  './SRC/components/SequenceOrderTask.css',
  './public/icon-192.png',
  './public/icon-512.png',
];

self.addEventListener('install', e => {
  e.waitUntil(
    caches.open(CACHE).then(c => c.addAll(STATIC)).then(() => self.skipWaiting())
  );
});

self.addEventListener('activate', e => {
  e.waitUntil(
    caches.keys()
      .then(keys => Promise.all(keys.filter(k => k !== CACHE).map(k => caches.delete(k))))
      .then(() => self.clients.claim())
  );
});

self.addEventListener('fetch', e => {
  if (e.request.method !== 'GET') return;
  const url = new URL(e.request.url);

  // Network-first for HTML/JS/CSS — всегда берём свежие версии
  const isDynamic = ['.html', '.js', '.css'].some(ext => url.pathname.endsWith(ext))
    || url.pathname === '/' || url.pathname.endsWith('/');

  if (isDynamic) {
    e.respondWith(
      fetch(e.request)
        .then(res => {
          if (res && res.status === 200) {
            const clone = res.clone();
            caches.open(CACHE).then(c => c.put(e.request, clone));
          }
          return res;
        })
        .catch(() => caches.match(e.request))
    );
    return;
  }

  // Cache-first для статических ресурсов (иконки, данные)
  e.respondWith(
    caches.match(e.request).then(cached => {
      if (cached) return cached;
      return fetch(e.request).then(res => {
        if (res && res.status === 200) {
          const clone = res.clone();
          caches.open(CACHE).then(c => c.put(e.request, clone));
        }
        return res;
      }).catch(() => caches.match('./index.html'));
    })
  );
});
